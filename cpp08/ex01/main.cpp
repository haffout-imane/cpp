#include "Span.hpp"

int main()
{
    try
    {
        Span sp = Span(10);
        sp.addNumber(3);
        sp.addNumber(5);
        sp.addNumber(12);
        sp.addNumber(13);
        std::cout << "Shortest span: " << sp.shortestSpan() << std::endl;
        std::cout << "Longest span: " << sp.longestSpan() << std::endl;
    }
    catch(const std::exception& e)
    {
        std::cerr << e.what() << '\n';
    }
    std::cout << "--------------------------------" << std::endl;
    try
    {
        std::vector<int> v;
        for(int i = 0; i < 10000; i++)
            v.push_back(i);
        Span sp = Span(10000);
        sp.addRange(v.begin(), v.end());
        std::cout << "Shortest span: " << sp.shortestSpan() << std::endl;
        std::cout << "Longest span: " << sp.longestSpan() << std::endl;
    }
    catch(const std::exception& e)
    {
        std::cerr << e.what() << '\n';
    }

    return 0;
}